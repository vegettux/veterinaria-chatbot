def cancelar_cita_confirmada_por_mascota(mascota_id: int) -> bool:
    conn = get_connection()
    try:
        c = conn.cursor()
        c.execute(
            paramstyle(
                """
                UPDATE citas
                SET estado = 'cancelada'
                WHERE mascota_id = %s AND estado = 'confirmada'
                """
            ),
            (mascota_id,),
        )
        affected = c.rowcount
        conn.commit()
        return affected > 0
    except Exception:
        try:
            conn.rollback()
        except Exception:
            pass
        return False
    finally:
        conn.close()



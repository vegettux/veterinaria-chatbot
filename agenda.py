def init_db():
    conn = get_connection()
    try:
        c = conn.cursor()

        c.execute(sql(
            """
            CREATE TABLE IF NOT EXISTS clientes (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                nombre TEXT NOT NULL,
                dni TEXT UNIQUE,
                telefono TEXT,
                email TEXT,
                direccion TEXT,
                created_at TEXT DEFAULT CURRENT_TIMESTAMP
            )
            """,
            """
            CREATE TABLE IF NOT EXISTS clientes (
                id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                nombre TEXT NOT NULL,
                dni TEXT UNIQUE,
                telefono TEXT,
                email TEXT,
                direccion TEXT,
                created_at TEXT DEFAULT CURRENT_TIMESTAMP
            )
            """
        ))

        c.execute(sql(
            """
            CREATE TABLE IF NOT EXISTS mascotas (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                cliente_id INTEGER NOT NULL,
                nombre TEXT NOT NULL,
                especie TEXT NOT NULL,
                sexo TEXT NOT NULL,
                peso REAL DEFAULT 0,
                edad INTEGER DEFAULT 0,
                tiene_microchip INTEGER DEFAULT 0,
                tiene_vacuna_rabia INTEGER DEFAULT 0,
                FOREIGN KEY (cliente_id) REFERENCES clientes(id) ON DELETE CASCADE
            )
            """,
            """
            CREATE TABLE IF NOT EXISTS mascotas (
                id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                cliente_id INTEGER NOT NULL,
                nombre TEXT NOT NULL,
                especie TEXT NOT NULL,
                sexo TEXT NOT NULL,
                peso REAL DEFAULT 0,
                edad INTEGER DEFAULT 0,
                tiene_microchip INTEGER DEFAULT 0,
                tiene_vacuna_rabia INTEGER DEFAULT 0,
                FOREIGN KEY (cliente_id) REFERENCES clientes(id) ON DELETE CASCADE
            )
            """
        ))

        c.execute(sql(
            """
            CREATE TABLE IF NOT EXISTS citas (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                fecha TEXT NOT NULL,
                cliente_id INTEGER NOT NULL,
                mascota_id INTEGER NOT NULL,
                tipo_cirugia TEXT NOT NULL,
                minutos INTEGER NOT NULL,
                estado TEXT DEFAULT 'confirmada',
                created_at TEXT DEFAULT CURRENT_TIMESTAMP,
                FOREIGN KEY (cliente_id) REFERENCES clientes(id) ON DELETE CASCADE,
                FOREIGN KEY (mascota_id) REFERENCES mascotas(id) ON DELETE CASCADE
            )
            """,
            """
            CREATE TABLE IF NOT EXISTS citas (
                id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                fecha TEXT NOT NULL,
                cliente_id INTEGER NOT NULL,
                mascota_id INTEGER NOT NULL,
                tipo_cirugia TEXT NOT NULL,
                minutos INTEGER NOT NULL,
                estado TEXT DEFAULT 'confirmada',
                created_at TEXT DEFAULT CURRENT_TIMESTAMP,
                FOREIGN KEY (cliente_id) REFERENCES clientes(id) ON DELETE CASCADE,
                FOREIGN KEY (mascota_id) REFERENCES mascotas(id) ON DELETE CASCADE
            )
            """
        ))

        c.execute(sql(
            """
            CREATE TABLE IF NOT EXISTS cambios_cita (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                cliente_id INTEGER,
                mascota_id INTEGER,
                fecha_antigua TEXT,
                fecha_nueva TEXT,
                fecha_cambio TEXT
            )
            """,
            """
            CREATE TABLE IF NOT EXISTS cambios_cita (
                id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                cliente_id INTEGER,
                mascota_id INTEGER,
                fecha_antigua TEXT,
                fecha_nueva TEXT,
                fecha_cambio TEXT
            )
            """
        ))

        # Índices útiles
        c.execute("CREATE INDEX IF NOT EXISTS idx_clientes_dni ON clientes(dni)")
        c.execute("CREATE INDEX IF NOT EXISTS idx_mascotas_cliente_id ON mascotas(cliente_id)")
        c.execute("CREATE INDEX IF NOT EXISTS idx_mascotas_cliente_nombre ON mascotas(cliente_id, nombre)")
        c.execute("CREATE INDEX IF NOT EXISTS idx_citas_fecha_estado ON citas(fecha, estado)")
        c.execute("CREATE INDEX IF NOT EXISTS idx_citas_mascota_estado ON citas(mascota_id, estado)")
        c.execute("CREATE INDEX IF NOT EXISTS idx_citas_cliente_fecha_estado ON citas(cliente_id, fecha, estado)")

        conn.commit()
    finally:
        conn.close()


def db_healthcheck() -> dict:
    try:
        conn = get_connection()
        try:
            c = conn.cursor()
            c.execute("SELECT 1 AS ok")
            row = c.fetchone()
            row_dict = row_to_dict(row)
            return {"status": "ok", "ping": row_dict["ok"] if row_dict else None}
        finally:
            conn.close()
    except Exception as exc:
        return {"status": "error", "error": str(exc)}



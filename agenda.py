def is_sqlite() -> bool:
    return bool(DATABASE_URL and DATABASE_URL.startswith("sqlite:///"))


def is_postgres() -> bool:
    return bool(
        DATABASE_URL
        and (
            DATABASE_URL.startswith("postgresql://")
            or DATABASE_URL.startswith("postgres://")
        )
    )


def get_sqlite_path() -> str:
    if not DATABASE_URL:
        raise RuntimeError("DATABASE_URL no está configurada.")

    raw_path = DATABASE_URL.replace("sqlite:///", "", 1)
    path = Path(raw_path)

    if not path.is_absolute():
        path = BASE_DIR / path

    return str(path)


def get_connection():
    if not DATABASE_URL:
        raise RuntimeError("DATABASE_URL no está configurada.")

    if is_sqlite():
        db_path = get_sqlite_path()
        conn = sqlite3.connect(db_path, timeout=10)
        conn.row_factory = sqlite3.Row
        conn.execute("PRAGMA foreign_keys = ON")
        return conn

    if is_postgres():
        if psycopg is None or dict_row is None:
            raise RuntimeError("Dependencia psycopg no disponible en runtime.")
        try:
            return psycopg.connect(DATABASE_URL, row_factory=dict_row)
        except Exception as exc:
            logger.exception("Error conectando a Postgres.")
            raise RuntimeError(f"No se pudo conectar a Postgres: {exc}") from exc

    raise RuntimeError(f"Formato de DATABASE_URL no soportado: {DATABASE_URL}")


def row_to_dict(row: Any) -> Optional[dict]:
    if row is None:
        return None
    if isinstance(row, dict):
        return row
    try:
        return dict(row)
    except Exception:
        return None


def rows_to_dicts(rows: list[Any]) -> list[dict]:
    resultado = []
    for row in rows:
        as_dict = row_to_dict(row)
        if as_dict is not None:
            resultado.append(as_dict)
    return resultado


def sql(query_sqlite: str, query_postgres: str) -> str:
    return query_sqlite if is_sqlite() else query_postgres


def paramstyle(query: str) -> str:
    if is_sqlite():
        return query.replace("%s", "?")
    return query


def execute_fetchone(query: str, params: tuple = ()) -> Optional[dict]:
    conn = get_connection()
    try:
        c = conn.cursor()
        c.execute(paramstyle(query), params)
        row = c.fetchone()
        return row_to_dict(row)
    finally:
        conn.close()


def execute_fetchall(query: str, params: tuple = ()) -> list[dict]:
    conn = get_connection()
    try:
        c = conn.cursor()
        c.execute(paramstyle(query), params)
        rows = c.fetchall()
        return rows_to_dicts(rows)
    finally:
        conn.close()



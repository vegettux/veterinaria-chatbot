def existe_cita_mascota(fecha_str: str, mascota_id: int) -> bool:
    row = execute_fetchone(
        """
        SELECT COUNT(*) AS total
        FROM citas
        WHERE fecha = %s AND mascota_id = %s AND estado = 'confirmada'
        """,
        (fecha_str, mascota_id),
    )
    return bool(row and row["total"] > 0)


def obtener_cita_confirmada_por_mascota(mascota_id: int) -> Optional[str]:
    row = execute_fetchone(
        """
        SELECT fecha
        FROM citas
        WHERE mascota_id = %s AND estado = 'confirmada'
        ORDER BY fecha DESC
        LIMIT 1
        """,
        (mascota_id,),
    )
    return row["fecha"] if row else None


def obtener_todas_citas_confirmadas_por_mascota(mascota_id: int) -> list[str]:
    rows = execute_fetchall(
        """
        SELECT fecha
        FROM citas
        WHERE mascota_id = %s AND estado = 'confirmada'
        ORDER BY fecha
        """,
        (mascota_id,),
    )
    return [row["fecha"] for row in rows]


def es_dia_operativo(fecha_str: str) -> tuple[bool, str]:
    try:
        fecha = datetime.strptime(fecha_str, "%Y-%m-%d").date()
    except ValueError:
        return False, "Formato de fecha incorrecto."

    if fecha.weekday() not in DIAS_OPERATIVOS:
        dias = ["lunes", "martes", "miércoles", "jueves", "viernes", "sábado", "domingo"]
        return False, f"El {dias[fecha.weekday()]} no es día operativo. Las cirugías son de lunes a jueves."

    return True, ""


def get_minutos_ocupados(fecha_str: str, conn=None) -> int:
    close_after = False
    if conn is None:
        conn = get_connection()
        close_after = True

    try:
        c = conn.cursor()
        c.execute(
            paramstyle(
                "SELECT COALESCE(SUM(minutos), 0) AS total FROM citas WHERE fecha = %s AND estado = 'confirmada'"
            ),
            (fecha_str,),
        )
        row = c.fetchone()
        row_dict = row_to_dict(row)
        return (row_dict["total"] if row_dict else 0) or 0
    finally:
        if close_after:
            conn.close()


def get_perros_dia(fecha_str: str, conn=None) -> int:
    close_after = False
    if conn is None:
        conn = get_connection()
        close_after = True

    try:
        c = conn.cursor()
        c.execute(
            paramstyle(
                """
                SELECT COUNT(*) AS total
                FROM citas c
                JOIN mascotas m ON c.mascota_id = m.id
                WHERE c.fecha = %s AND c.estado = 'confirmada' AND m.especie = 'perro'
                """
            ),
            (fecha_str,),
        )
        row = c.fetchone()
        row_dict = row_to_dict(row)
        return (row_dict["total"] if row_dict else 0) or 0
    finally:
        if close_after:
            conn.close()



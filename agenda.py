def buscar_cliente_por_dni(dni: str) -> Optional[dict]:
    return execute_fetchone(
        "SELECT id, nombre, dni, telefono, email, direccion FROM clientes WHERE UPPER(dni) = %s",
        (dni.strip().upper(),),
    )


def buscar_cliente_por_nombre(nombre: str) -> Optional[dict]:
    return execute_fetchone(
        "SELECT id, nombre, dni, telefono, email, direccion FROM clientes WHERE LOWER(nombre) LIKE %s LIMIT 1",
        (f"%{nombre.strip().lower()}%",),
    )


def registrar_cliente(nombre: str, dni: str, telefono: str, email: str, direccion: str) -> tuple[bool, str]:
    conn = get_connection()
    try:
        c = conn.cursor()

        if is_sqlite():
            c.execute(
                """
                INSERT INTO clientes (nombre, dni, telefono, email, direccion)
                VALUES (?, ?, ?, ?, ?)
                """,
                (
                    nombre.strip(),
                    dni.strip().upper(),
                    telefono.strip(),
                    email.strip(),
                    direccion.strip(),
                ),
            )
            cliente_id = c.lastrowid
        else:
            c.execute(
                """
                INSERT INTO clientes (nombre, dni, telefono, email, direccion)
                VALUES (%s, %s, %s, %s, %s)
                RETURNING id
                """,
                (
                    nombre.strip(),
                    dni.strip().upper(),
                    telefono.strip(),
                    email.strip(),
                    direccion.strip(),
                ),
            )
            row = c.fetchone()
            row_dict = row_to_dict(row)
            cliente_id = row_dict["id"] if row_dict else None

        conn.commit()
        return True, str(cliente_id) if cliente_id is not None else ""
    except Exception as e:
        error_text = str(e).lower()
        if "duplicate key value" in error_text or "unique constraint failed" in error_text:
            return False, "Ya existe un cliente con ese DNI."
        raise
    finally:
        conn.close()


